# Déclaration de la fonction, avec 2 paramètres, a et b
def addition(a=5, b=10):
	print(a + b)

# Appel de la fonction. On passe deux valeurs (appelés arguments)
# qui seront passés aux paramètres a et b à l'intérieur de la fonction
addition(5, 10)
# Appel de la fonction avec des valeurs d'arguments différentes
addition(10, 20)